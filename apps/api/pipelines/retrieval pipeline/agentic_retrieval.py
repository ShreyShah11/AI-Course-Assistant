def _get_bm25_text(meta: dict, source_key: str) -> str:
    """Extract the most keyword-rich text for BM25 indexing."""
    if source_key == "qna":
        qs  = meta.get("questions", []) or []
        ans = meta.get("answers",   []) or []
        parts = ([qs] if isinstance(qs, str) else list(qs)) + \
                ([ans] if isinstance(ans, str) else list(ans))
        combined = " ".join(str(p) for p in parts)
        return combined or meta.get("raw_text", "")

    elif source_key == "image":
        return " ".join(filter(None, [
            meta.get("raw_text", ""),
            meta.get("gemini_summary", ""),
            meta.get("gemini_topic", ""),
            " ".join(meta.get("gemini_keywords", []) or []),
        ]))

    elif source_key == "handwritten":
        return " ".join(filter(None, [
            meta.get("transcript", meta.get("raw_text", "")),
            meta.get("topic", ""),
            meta.get("summary", ""),
        ]))

    elif source_key == "documents":
        return " ".join(filter(None, [
            meta.get("raw_text", meta.get("text_preview", "")),
            meta.get("section_title", ""),
            meta.get("keywords", ""),
        ]))

    # audio / youtube — less reliable for BM25, but use if requested
    return (
        meta.get("raw_text") or
        meta.get("text") or
        meta.get("transcript") or
        meta.get("text_preview") or
        ""
    )


def bm25_retrieve(
    query: str,
    corpus_hits: list[dict],
    top_k: int,
    source_key: str,
) -> list[dict]:
    """BM25 re-ranking over a dense candidate corpus."""
    if not corpus_hits or top_k == 0:
        return []
    texts = [_get_bm25_text(h["metadata"], source_key) for h in corpus_hits]
    tokenized_corpus = [_tokenize(t) for t in texts]
    tokenized_query  = _tokenize(query)
    if not tokenized_query:
        return corpus_hits[:top_k]
    bm25 = BM25Okapi(tokenized_corpus)
    scores = bm25.get_scores(tokenized_query)
    max_s = float(scores.max()) if scores.max() > 0 else 1.0
    normed = scores / max_s
    ranked = sorted(range(len(corpus_hits)), key=lambda i: normed[i], reverse=True)
    results = []
    for rank_idx, corpus_idx in enumerate(ranked[:top_k]):
        hit = dict(corpus_hits[corpus_idx])
        hit["bm25_score"] = float(normed[corpus_idx])
        hit["bm25_rank"]  = rank_idx + 1
        results.append(hit)
    return results


