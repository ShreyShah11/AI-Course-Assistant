@dataclass
class RetrievedChunk:
    """One retrieved chunk, citation-ready."""
    chunk_id:          str
    source_key:        str
    namespace:         str
    score:             float           # final combined score
    rrf_score:         float
    retrieval_methods: list[str]

    # Content
    text:              str = ""
    questions:         list[str] = field(default_factory=list)
    answers:           list[str] = field(default_factory=list)

    # Universal provenance
    source_file:       str = ""
    course_id:         str = ""
    page_range:        str = ""
    section_title:     str = ""
    topic:             str = ""
    chunk_type:        str = ""
    keywords:          list[str] = field(default_factory=list)

    # QnA-specific
    year_hint:         Optional[int] = None
    marks_hint:        Optional[int] = None
    difficulty:        str = ""

    # Image-specific
    image_type:        str = ""
    image_summary:     str = ""
    ocr_confidence:    float = 0.0

    # Audio-specific
    strategy:          str = ""
    start_seconds:     float = 0.0
    end_seconds:       float = 0.0
    start_label:       str = ""
    end_label:         str = ""
    lecture_title:     str = ""
    professor:         str = ""
    week_number:       Optional[int] = None

    # YouTube-specific
    video_id:          str = ""
    video_title:       str = ""
    channel_author:    str = ""
    deep_link:         str = ""
    chapter_title:     str = ""

    # Handwritten-specific
    ink_quality:       str = ""
    writing_style:     str = ""
    content_type:      str = ""
    has_equations:     bool = False
    has_diagrams:      bool = False
    model_used:        str = ""

    # Documents-specific
    has_table:         bool = False
    has_image:         bool = False
    has_list:          bool = False

    @property
    def citation_label(self) -> str:
        if self.source_key == "qna":
            parts = [self.source_file or "Q&A Sheet"]
            if self.page_range:
                parts.append(f"pp. {self.page_range}")
            if self.year_hint:
                parts.append(str(self.year_hint))
            if self.marks_hint and self.marks_hint > 0:
                parts.append(f"[{self.marks_hint} marks]")
            return " · ".join(parts)

        elif self.source_key == "image":
            parts = [self.source_file or "Scanned Material"]
            if self.page_range:
                parts.append(f"pp. {self.page_range}")
            if self.image_type:
                parts.append(self.image_type.replace("_", " ").title())
            return " · ".join(parts)

        elif self.source_key == "audio":
            parts = [self.lecture_title or self.source_file or "Lecture"]
            if self.start_label:
                parts.append(f"@ {self.start_label}–{self.end_label}")
            if self.professor:
                parts.append(self.professor)
            if self.week_number:
                parts.append(f"Week {self.week_number}")
            return " · ".join(parts)

        elif self.source_key == "youtube":
            parts = [self.video_title or "YouTube Lecture"]
            if self.start_label:
                parts.append(f"@ {self.start_label}")
            if self.chapter_title and self.chapter_title not in ("Unknown", ""):
                parts.append(self.chapter_title)
            return " · ".join(parts)

        elif self.source_key == "documents":
            parts = [self.source_file or "Course Document"]
            if self.page_range:
                parts.append(f"pp. {self.page_range}")
            if self.section_title:
                parts.append(f"§ {self.section_title}")
            return " · ".join(parts)

        elif self.source_key == "handwritten":
            parts = [self.source_file or "Handwritten Notes"]
            if self.page_range:
                parts.append(f"p. {self.page_range}")
            if self.topic:
                parts.append(self.topic)
            return " · ".join(parts)

        return self.source_file or self.source_key

    @property
    def display_text(self) -> str:
        if self.source_key == "qna" and (self.questions or self.answers):
            parts = [f"Q{i+1}: {q}" for i, q in enumerate(self.questions)]
            parts += [f"A{i+1}: {a}" for i, a in enumerate(self.answers)]
            return "\n".join(parts) if parts else self.text

        elif self.source_key == "audio" and self.start_label:
            return f"[Lecture @ {self.start_label}–{self.end_label}]\n{self.text}"

        elif self.source_key == "youtube" and self.deep_link:
            return f"[YouTube @ {self.start_label} — {self.deep_link}]\n{self.text}"

        elif self.source_key == "image" and self.image_summary:
            return f"[Image: {self.image_type}]\nSummary: {self.image_summary}\n\nExtracted text:\n{self.text}"

        elif self.source_key == "handwritten":
            prefix = "[has equations] " if self.has_equations else ""
            return f"{prefix}{self.text}"

        return self.text

    def to_dict(self) -> dict:
        return {
            "chunk_id":          self.chunk_id,
            "source_key":        self.source_key,
            "score":             round(self.score, 4),
            "rrf_score":         round(self.rrf_score, 4),
            "retrieval_methods": self.retrieval_methods,
            "citation_label":    self.citation_label,
            "display_text":      self.display_text[:800],
            "source_file":       self.source_file,
            "course_id":         self.course_id,
            "page_range":        self.page_range,
            "section_title":     self.section_title,
            "topic":             self.topic,
            "chunk_type":        self.chunk_type,
            "keywords":          self.keywords,
            "year_hint":         self.year_hint,
            "marks_hint":        self.marks_hint,
            "image_type":        self.image_type,
            "image_summary":     self.image_summary[:300] if self.image_summary else "",
            "start_label":       self.start_label,
            "lecture_title":     self.lecture_title,
            "professor":         self.professor,
            "video_title":       self.video_title,
            "deep_link":         self.deep_link,
            "chapter_title":     self.chapter_title,
            "has_equations":     self.has_equations,
            "has_diagrams":      self.has_diagrams,
            "has_table":         self.has_table,
        }


@dataclass
class RetrievalResult:
    """Top-level result returned to the caller."""
    query:            str
    mode:             RetrievalMode
    plan:             QueryPlan
    chunks:           list[RetrievedChunk]
    total_candidates: int
    latency_ms:       float
    source_breakdown: dict[str, int] = field(default_factory=dict)

    def to_context_string(self, max_chunks: Optional[int] = None) -> str:
        selected = self.chunks[:max_chunks] if max_chunks else self.chunks
        parts: list[str] = []
        for i, chunk in enumerate(selected, 1):
            header = (
                f"[{i}] {chunk.source_key.upper()} SOURCE\n"
                f"     Citation : {chunk.citation_label}\n"
                f"     Type     : {chunk.chunk_type or '—'}\n"
                f"     Score    : {chunk.score:.3f}\n"
                f"     Methods  : {', '.join(chunk.retrieval_methods)}"
            )
            parts.append(f"{header}\n\n{chunk.display_text.strip()}")
        divider = "\n" + "─" * 65 + "\n"
        return "\n" + divider.join(parts)

    def to_answer_prompt(self, system_instruction: str = "") -> str:
        plan = self.plan
        mode_instructions = {
            RetrievalMode.ASK: (
                "Answer the student's question clearly and completely, citing every "
                "factual claim with [N] where N is the source number."
            ),
            RetrievalMode.QUIZ: (
                f"Generate {plan.quiz_config.num_questions if plan.quiz_config else 5} "
                "quiz questions based on the retrieved content. "
                "Include answer keys and brief explanations. "
                "Cite source material with [N] notation."
            ),
            RetrievalMode.SUMMARIZE: (
                "Write a comprehensive summary of the topic using the retrieved content. "
                "Cite sources with [N] notation. Cover all key concepts found."
            ),
        }
        default_sys = (
            "You are a knowledgeable university course assistant. "
            + mode_instructions.get(self.mode, mode_instructions[RetrievalMode.ASK])
        )
        sys_text = system_instruction or default_sys
        prompt = (
            f"MODE: {self.mode.value.upper()}\n"
            f"STUDENT QUERY: {self.query}\n\n"
            f"QUERY CATEGORY: {plan.query_category}\n"
            f"ANSWER FORMAT: {plan.expected_answer_format}\n"
            f"ANSWER LENGTH: {plan.expected_answer_length}\n"
            f"KEY CONCEPTS: {', '.join(plan.key_concepts)}\n\n"
        )
        if self.mode == RetrievalMode.QUIZ and plan.quiz_config:
            qc = plan.quiz_config
            prompt += (
                f"QUIZ CONFIG:\n"
                f"  Types: {', '.join(qc.question_types)}\n"
                f"  Count: {qc.num_questions}\n"
                f"  Difficulty: {qc.difficulty_distribution}\n"
                f"  Include answer key: {qc.include_answer_key}\n\n"
            )
        if self.mode == RetrievalMode.SUMMARIZE and plan.summary_config:
            sc = plan.summary_config
            prompt += (
                f"SUMMARY CONFIG:\n"
                f"  Depth: {sc.depth}\n"
                f"  Format: {sc.format}\n"
                f"  Include key terms: {sc.include_key_terms}\n"
                f"  Include examples: {sc.include_examples}\n\n"
            )
        prompt += (
            f"RETRIEVED CONTEXT ({len(self.chunks)} sources):\n"
            f"{self.to_context_string()}\n\n"
            "Now write your response. Every factual claim MUST cite a source [N]."
        )
        return f"{sys_text}\n\n{prompt}"


