class QueryPlan(BaseModel):
    """Complete retrieval plan produced by the Planner Agent."""

    # ── Intent ───────────────────────────────────────────────────────────────
    query_intent: str = Field(..., description="What the student is really asking for, in one sentence.")
    query_category: Literal[
        "theoretical_explanation",
        "definition_or_concept",
        "problem_or_derivation",
        "comparison",
        "worked_example",
        "exam_question",
        "visual_or_diagram",
        "procedural_steps",
        "application",
        "topic_overview",
        "clarification",
    ] = Field(..., description="Primary category of the query.")
    key_concepts: list[str] = Field(..., min_length=2, max_length=10, description="Core concepts central to this query.")
    complexity_level: Literal["basic", "intermediate", "advanced"] = Field(
        ...,
        description="basic=2 sub-queries, intermediate=3-4, advanced=4-5.",
    )

    # ── Sub-queries ──────────────────────────────────────────────────────────
    sub_queries: list[SubQuery] = Field(
        ..., min_length=1, max_length=5,
        description=(
            "2-5 semantically diverse sub-queries targeting different facets. "
            "NEVER paraphrase — each must retrieve different content. "
            "Always include: one exam/textbook phrasing AND one conversational explanation phrasing."
        ),
    )

    # ── Per-source retrieval config ──────────────────────────────────────────
    source_budgets: list[SourceBudget] = Field(
        ..., min_length=1, max_length=6,
        description=(
            "Retrieval configuration for each relevant source. "
            "Include only sources genuinely likely to have relevant content."
        ),
    )

    # ── Mode-specific configs ────────────────────────────────────────────────
    quiz_config: Optional[QuizConfig] = Field(
        default=None,
        description="Populated only when mode=quiz.",
    )
    summary_config: Optional[SummaryConfig] = Field(
        default=None,
        description="Populated only when mode=summarize.",
    )

    # ── Answer generation hints ──────────────────────────────────────────────
    expected_answer_format: Literal[
        "structured_explanation",
        "definition_then_example",
        "comparison_table",
        "worked_solution",
        "conceptual_prose",
        "quiz_questions",
        "topic_summary",
        "bullet_summary",
    ] = Field(..., description="Recommended format for the final answer.")
    expected_answer_length: Literal["short", "medium", "long"] = Field(
        ..., description="short=1-2 paragraphs, medium=3-5 paragraphs, long=full structured."
    )
    domain_keywords: list[str] = Field(
        ..., min_length=1, max_length=15,
        description="Keywords that SHOULD appear in a good answer — used for quality validation.",
    )

    # ── High-value signals for scoring ───────────────────────────────────────
    high_value_signals: list[str] = Field(
        ...,
        description=(
            "Content signals that make a chunk MORE valuable. "
            "E.g. ['contains definition', 'has worked example', 'mentions marks/exam year', "
            "'contains pseudocode', 'shows step-by-step']."
        ),
    )
    low_value_signals: list[str] = Field(
        ...,
        description=(
            "Content signals that make a chunk LESS valuable. "
            "E.g. ['administrative content', 'unrelated subtopic', 'only tangential mention']."
        ),
    )

    @field_validator("key_concepts", "domain_keywords", "sub_queries", "source_budgets", mode="before")
    @classmethod
    def trim_overlong_planner_lists(cls, value, info: ValidationInfo):
        if not isinstance(value, list):
            return value
        limits = {
            "key_concepts": 10,
            "domain_keywords": 15,
            "sub_queries": 5,
            "source_budgets": 6,
        }
        limit = limits.get(info.field_name)
        if limit is None:
            return value
        return value[:limit]


