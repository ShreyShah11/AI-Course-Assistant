# Metadata inference helpers

def _infer_source_key(meta: dict) -> str:
    """Infer pipeline origin from metadata fingerprint."""
    if "video_id" in meta or "deep_link" in meta:
        return "youtube"
    if "ink_quality" in meta or "writing_style" in meta or "model_used" in meta:
        return "handwritten"
    if "strategy" in meta and meta.get("strategy") in {"sliding_window", "concept_block", "lecture_summary"}:
        return "audio"
    if "gemini_summary" in meta or "gemini_topic" in meta or "ocr_confidence" in meta:
        return "image"
    if meta.get("chunk_type") in {"single_qa", "multi_qa", "passage"} or "year_hint" in meta:
        return "qna"
    if "section_title" in meta or "has_table" in meta or "ingested_at" in meta:
        return "documents"
    return "documents"


def _parse_page_range(meta: dict) -> str:
    pr = meta.get("page_range")
    if isinstance(pr, list) and len(pr) == 2:
        return f"{pr[0]}–{pr[1]}"
    ps = meta.get("page_start")
    pe = meta.get("page_end")
    if ps is not None and pe is not None:
        return f"{int(ps)}–{int(pe)}" if ps != pe else str(int(ps))
    pn = meta.get("page_number")
    if pn is not None:
        return str(int(pn))
    return str(pr) if pr else ""


def _seconds_to_label(sec: float) -> str:
    total = int(sec)
    h, rem = divmod(total, 3600)
    m, s   = divmod(rem, 60)
    return f"{h:02d}:{m:02d}:{s:02d}" if h else f"{m:02d}:{s:02d}"


def _build_retrieved_chunk(
    chunk_id:          str,
    meta:              dict,
    namespace:         str,
    source_key:        str,
    rrf_score:         float,
    retrieval_methods: list[str],
) -> RetrievedChunk:
    text = (
        meta.get("raw_text") or meta.get("text") or
        meta.get("transcript") or meta.get("text_preview") or ""
    )
    keywords_raw = meta.get("keywords", meta.get("gemini_keywords", []))
    if isinstance(keywords_raw, str):
        keywords = [k.strip() for k in keywords_raw.split(",") if k.strip()]
    elif isinstance(keywords_raw, list):
        keywords = keywords_raw
    else:
        keywords = []

    chunk = RetrievedChunk(
        chunk_id          = chunk_id,
        source_key        = source_key,
        namespace         = namespace,
        score             = round(rrf_score, 4),
        rrf_score         = rrf_score,
        retrieval_methods = retrieval_methods,
        text              = text,
        source_file       = meta.get("source_file", meta.get("file_name", "")),
        course_id         = meta.get("course_id", ""),
        page_range        = _parse_page_range(meta),
        section_title     = meta.get("section_title", meta.get("section", "")),
        topic             = meta.get("topic", meta.get("topic_label", meta.get("gemini_topic", ""))),
        chunk_type        = meta.get("chunk_type", meta.get("strategy", meta.get("content_type", ""))),
        keywords          = keywords[:10],
    )

    if source_key == "qna":
        qs = meta.get("questions", [])
        an = meta.get("answers",   [])
        chunk.questions  = qs if isinstance(qs, list) else ([qs] if qs else [])
        chunk.answers    = an if isinstance(an, list) else ([an] if an else [])
        chunk.year_hint  = meta.get("year_hint")
        chunk.marks_hint = meta.get("marks_hint")
        chunk.difficulty = meta.get("difficulty_hint", "unknown")

    elif source_key == "image":
        signals = meta.get("gemini_content_signals", []) or []
        chunk.image_type    = meta.get("image_type", signals[0] if signals else "")
        chunk.image_summary = meta.get("gemini_summary", "")
        chunk.ocr_confidence = float(meta.get("ocr_confidence", meta.get("mean_confidence", 0)) or 0) / 100

    elif source_key == "audio":
        chunk.strategy     = meta.get("strategy", "")
        chunk.start_seconds = float(meta.get("start_seconds", 0.0))
        chunk.end_seconds   = float(meta.get("end_seconds",   0.0))
        chunk.start_label   = meta.get("start_label", _seconds_to_label(chunk.start_seconds))
        chunk.end_label     = meta.get("end_label",   _seconds_to_label(chunk.end_seconds))
        chunk.lecture_title = meta.get("lecture_title", "")
        chunk.professor     = meta.get("professor", "")
        chunk.week_number   = meta.get("week_number")

    elif source_key == "youtube":
        chunk.video_id      = meta.get("video_id", "")
        chunk.video_title   = meta.get("video_title", "")
        chunk.channel_author = meta.get("author", "")
        chunk.deep_link     = meta.get("deep_link", "")
        chunk.chapter_title = meta.get("chapter_title", "")
        chunk.start_label   = meta.get("start_label", "")
        chunk.end_label     = meta.get("end_label",   "")
        chunk.text          = meta.get("raw_text", chunk.text)

    elif source_key == "handwritten":
        chunk.ink_quality   = meta.get("ink_quality", "")
        chunk.writing_style = meta.get("writing_style", "")
        chunk.content_type  = meta.get("content_type", "")
        chunk.has_equations = bool(meta.get("has_equations", False))
        chunk.has_diagrams  = bool(meta.get("has_diagrams", False))
        chunk.model_used    = meta.get("model_used", "")
        chunk.text = (
            meta.get("structured_markdown") or
            meta.get("transcript") or chunk.text
        )

    elif source_key == "documents":
        chunk.has_table = bool(meta.get("has_table", False))
        chunk.has_image = bool(meta.get("has_image", False))
        chunk.has_list  = bool(meta.get("has_list",  False))

    return chunk


