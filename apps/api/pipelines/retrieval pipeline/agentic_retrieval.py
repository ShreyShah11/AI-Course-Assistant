"""
=====================================================================
 University AI Course Assistant — Retrieval Pipeline
=====================================================================
Supports three interaction modes:
  • ask        — Answer questions with explanations
  • quiz        — Generate quiz questions on a topic
  • summarize   — Summarise a topic

Six knowledge source namespaces:
  qna, image, audio, youtube, documents, handwritten

Pipeline:
  1. Planner Agent (Gemini) → structured QueryPlan via Pydantic
      - mode-aware intent analysis
      - 2-5 diverse sub-queries (Jaccard-deduped)
      - per-source budgets with dense_k, bm25_k, use_mmr, priority_weight
      - per-query source priority hints
      - answer format guidance
  2. Embed all sub-queries in parallel
  3. Fan-out: for each (sub-query × source) pair
      a. Dense ANN retrieval (Pinecone)
      b. BM25 on the dense corpus (rank-bm25)
      c. MMR diversity filter on dense results (when use_mmr=True)
  4. Metadata-boosted RRF fusion across all ranked lists
  5. Return citation-ready RetrievalResult

Install:
    pip install google-genai pinecone rank-bm25 pydantic numpy

Usage:
    from retrieval_pipeline import retrieve, RetrievalMode

    result = retrieve(
        query="What is TCP three-way handshake?",
        course_id="CN301",
        mode=RetrievalMode.ASK,
        top_k=8,
    )
    print(result.to_context_string())
    print(result.to_answer_prompt())
"""

