# Step 5 — MMR diversity filtering
# ─────────────────────────────────────────────────────────────────────────────

def mmr_filter(
    candidates:  list[dict],
    k:           int,
    lambda_:     float = 0.6,
    source_key:  str   = "",
) -> list[dict]:
    """Maximal Marginal Relevance selection with source-aware redundancy heuristics."""
    if len(candidates) <= k:
        return candidates
    relevances = [c.get("score", 0.0) for c in candidates]
    selected: list[int] = [int(np.argmax(relevances))]
    remaining = [i for i in range(len(candidates)) if i != selected[0]]
    while len(selected) < k and remaining:
        best_i, best_mmr = -1, -float("inf")
        for i in remaining:
            rel = relevances[i]
            max_sim = 0.0
            for j in selected:
                mi = candidates[i].get("metadata", {})
                mj = candidates[j].get("metadata", {})
                same_file = (
                    mi.get("source_file", "") == mj.get("source_file", "") or
                    mi.get("file_name", "")   == mj.get("file_name", "")
                )
                idx_i = mi.get("chunk_index", mi.get("window_index", -99))
                idx_j = mj.get("chunk_index", mj.get("window_index", -99))
                adjacent = (
                    isinstance(idx_i, int) and
                    isinstance(idx_j, int) and
                    abs(idx_i - idx_j) <= 2
                )
                sim = 0.85 if (same_file and adjacent) else (0.5 if same_file else relevances[i] * relevances[j])
                max_sim = max(max_sim, sim)
            mmr_score = lambda_ * rel - (1 - lambda_) * max_sim
            if mmr_score > best_mmr:
                best_mmr, best_i = mmr_score, i
        if best_i >= 0:
            selected.append(best_i)
            remaining.remove(best_i)
    return [candidates[i] for i in selected]


# ─────────────────────────────────────────────────────────────────────────────
# Step 6 — Metadata-boosted Reciprocal Rank Fusion
# ─────────────────────────────────────────────────────────────────────────────

def _metadata_boost(meta: dict, source_key: str, high_value_signals: list[str]) -> float:
    """
    Small multiplicative boost based on metadata signals.
    Keeps source-aware ranking deterministic and local.
    """
    boost = 1.0
    signals_lower = [s.lower() for s in high_value_signals]

    if source_key == "qna":
        if meta.get("marks_hint") and int(meta.get("marks_hint", 0)) > 0:
            boost *= 1.12   # exam questions with explicit marks are high-value
        if meta.get("year_hint"):
            boost *= 1.05
        if meta.get("chunk_type") in ("single_qa", "multi_qa"):
            if any("exam" in s or "question" in s for s in signals_lower):
                boost *= 1.10

    elif source_key == "handwritten":
        if meta.get("has_equations") and any("equation" in s or "formula" in s for s in signals_lower):
            boost *= 1.15
        if meta.get("content_type") == "equation":
            boost *= 1.08
        if meta.get("confidence", 1.0) < 0.6:
            boost *= 0.85   # penalise low-confidence OCR

    elif source_key == "image":
        conf = float(meta.get("ocr_confidence", meta.get("mean_confidence", 80))) / 100
        if conf < 0.65:
            boost *= 0.80
        if meta.get("has_formula") and any("formula" in s or "equation" in s for s in signals_lower):
            boost *= 1.12

    elif source_key == "audio":
        if meta.get("strategy") == "concept_block":
            boost *= 1.08   # concept blocks are more focused than sliding windows
        avg_conf = float(meta.get("avg_confidence", 0.8))
        if avg_conf < 0.7:
            boost *= 0.90

    elif source_key == "documents":
        if meta.get("has_table") and any("table" in s or "comparison" in s for s in signals_lower):
            boost *= 1.10

    return boost


def reciprocal_rank_fusion(
    ranked_lists:       list[list[tuple[str, dict]]],
    weights:            list[float],
    source_keys:        list[str],
    high_value_signals: list[str],
    k: int = RRF_K,
) -> dict[str, tuple[float, dict]]:
    """RRF with metadata-aware boosting."""
    scores:     dict[str, float] = {}
    meta_store: dict[str, dict]  = {}
    sk_store:   dict[str, str]   = {}

    for ranked_list, weight, sk in zip(ranked_lists, weights, source_keys):
        for rank, (chunk_id, metadata) in enumerate(ranked_list, start=1):
            boost = _metadata_boost(metadata, sk, high_value_signals)
            scores[chunk_id] = scores.get(chunk_id, 0.0) + weight * boost / (k + rank)
            if chunk_id not in meta_store:
                meta_store[chunk_id] = metadata
                sk_store[chunk_id]   = sk

    return {cid: (scores[cid], meta_store[cid]) for cid in scores}


