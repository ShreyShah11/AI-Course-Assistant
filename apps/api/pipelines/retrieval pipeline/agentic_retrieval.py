_STOPWORDS = {
    "a","an","the","is","in","on","of","to","for","and","or","with","that",
    "this","it","as","at","be","by","from","its","was","are","were","has",
    "have","had","not","but","can","will","just","do","did","so","if","we",
    "you","he","she","they","what","how","why","when","where","which","who",
}


def _tokenize(text: str) -> list[str]:
    tokens = re.findall(r"[a-zA-Z0-9_]{2,}", text.lower())
    return [t for t in tokens if t not in _STOPWORDS]


def _jaccard(a: str, b: str) -> float:
    sa, sb = set(_tokenize(a)), set(_tokenize(b))
    if not sa or not sb:
        return 0.0
    return len(sa & sb) / len(sa | sb)


def _deduplicate_sub_queries(sub_queries: list[SubQuery], threshold: float = 0.72) -> list[SubQuery]:
    kept: list[SubQuery] = []
    for sq in sub_queries:
        if all(_jaccard(sq.text, k.text) < threshold for k in kept):
            kept.append(sq)
        else:
            logger.debug("Dropped near-duplicate sub-query: %r", sq.text)
    return kept


