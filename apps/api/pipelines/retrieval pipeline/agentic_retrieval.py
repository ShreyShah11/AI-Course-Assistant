def dense_retrieve(
    query_vec:       list[float],
    namespace:       str,
    top_k:           int,
    course_id:       str,
    index_name:      str = PINECONE_INDEX_NAME,
) -> list[dict]:
    """ANN search in Pinecone for one (query_vec, namespace) pair."""
    index = _pinecone_index(index_name)
    query_kwargs: dict[str, Any] = {
        "vector": query_vec,
        "top_k": top_k,
        "namespace": namespace,
        "include_metadata": True,
    }
    try:
        results = index.query(**query_kwargs)
        return [
            {"id": m.id, "score": float(m.score), "metadata": m.metadata or {}}
            for m in results.matches
        ]
    except Exception as exc:
        logger.warning("Dense retrieve failed (ns=%s): %s", namespace, exc)
        return []


