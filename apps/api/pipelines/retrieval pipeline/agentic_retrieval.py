# Main retrieve() function
# ─────────────────────────────────────────────────────────────────────────────

def retrieve(
    query:      str,
    course_id:  str,
    mode:       RetrievalMode = RetrievalMode.ASK,
    top_k:      int   = 8,
    index_name: str | None = None,
    verbose:    bool  = False,
) -> RetrievalResult:
    """
    Full agentic RAG retrieval pipeline.

    Parameters
    ----------
    query      : Student's natural language question or topic.
    course_id  : Selects the course-specific Pinecone index.
    mode       : RetrievalMode.ASK | QUIZ | SUMMARIZE
    top_k      : Final number of chunks returned.
    index_name : Optional Pinecone index override. Defaults to the course index.
    verbose    : Print step-by-step progress.

    Returns
    -------
    RetrievalResult with citation-ready chunks and a complete answer prompt.
    """
    t_start = time.perf_counter()
    target_index_name = index_name or get_course_index_name(course_id)

    # ── 1. Planner ─────────────────────────────────────────────────────────
    if verbose:
        print(f"\n{'='*65}")
        print(f"[1/5] Planner Agent | mode={mode.value} | query={query!r}")
    plan = run_planner(query, course_id, mode)
    plan.sub_queries = _deduplicate_sub_queries(plan.sub_queries)

    if verbose:
        print(f"      Intent    : {plan.query_intent[:90]}")
        print(f"      Category  : {plan.query_category}  ({plan.complexity_level})")
        print(f"      Sub-queries ({len(plan.sub_queries)}):")
        for sq in plan.sub_queries:
            print(f"        [{sq.facet}] {sq.text!r}")
        print(f"      Sources   : {[b.source_key for b in plan.source_budgets]}")

    # ── 2. Embed sub-queries ───────────────────────────────────────────────
    if verbose:
        print(f"[2/5] Embedding {len(plan.sub_queries)} sub-queries …")
    query_vecs: dict[str, list[float]] = {sq.text: _embed(sq.text) for sq in plan.sub_queries}

    # ── 3. Multi-retrieval fan-out ─────────────────────────────────────────
    if verbose:
        print(f"[3/5] Retrieving from {len(plan.source_budgets)} source(s) …")

    rrf_inputs:       list[tuple[list[tuple[str, dict]], float, str]] = []
    chunk_methods:    dict[str, set[str]]  = {}
    chunk_meta_store: dict[str, dict]      = {}
    chunk_namespace:  dict[str, str]       = {}
    chunk_sourcekey:  dict[str, str]       = {}

    for budget in plan.source_budgets:
        sk = budget.source_key
        if sk not in SOURCE_NAMESPACES:
            logger.warning("Unknown source key: %r — skipping", sk)
            continue
        ns = SOURCE_NAMESPACES[sk]

        for sq in plan.sub_queries:
            q_vec = query_vecs[sq.text]
            fetch_k = budget.dense_k * BM25_CORPUS_MULTIPLIER if budget.bm25_k > 0 else budget.dense_k

            # Dense retrieval
            dense_hits = dense_retrieve(
                query_vec     = q_vec,
                namespace     = ns,
                top_k         = min(fetch_k, 40),
                course_id     = course_id,
                index_name    = target_index_name,
            )

            for h in dense_hits:
                cid = h["id"]
                chunk_meta_store[cid] = h["metadata"]
                chunk_namespace[cid]  = ns
                chunk_sourcekey[cid]  = sk
                chunk_methods.setdefault(cid, set()).add("dense")

            # MMR filtering
            if budget.use_mmr and len(dense_hits) > budget.mmr_k:
                before = len(dense_hits)
                dense_hits = mmr_filter(
                    candidates = dense_hits,
                    k          = budget.mmr_k,
                    lambda_    = budget.mmr_lambda,
                    source_key = sk,
                )
                if verbose:
                    print(f"        MMR [{sk}]: {before} → {len(dense_hits)}")
                for h in dense_hits:
                    chunk_methods.setdefault(h["id"], set()).add("mmr")

            dense_ranked: list[tuple[str, dict]] = [
                (h["id"], h["metadata"]) for h in dense_hits[:budget.dense_k]
            ]
            if dense_ranked:
                rrf_inputs.append((dense_ranked, budget.priority_weight, sk))

            # BM25
            if budget.bm25_k > 0 and dense_hits:
                bm25_hits = bm25_retrieve(
                    query        = sq.text,
                    corpus_hits  = dense_hits,
                    top_k        = budget.bm25_k,
                    source_key   = sk,
                )
                for h in bm25_hits:
                    cid = h["id"]
                    chunk_methods.setdefault(cid, set()).add("bm25")
                    chunk_meta_store[cid] = h["metadata"]
                    chunk_namespace[cid]  = ns
                    chunk_sourcekey[cid]  = sk

                bm25_weight_mult = {
                    "qna": 1.4, "image": 1.2, "documents": 1.2,
                    "handwritten": 1.1, "audio": 0.9, "youtube": 0.8,
                }.get(sk, 1.0)
                bm25_ranked: list[tuple[str, dict]] = [
                    (h["id"], h["metadata"]) for h in bm25_hits
                ]
                if bm25_ranked:
                    rrf_inputs.append((bm25_ranked, budget.priority_weight * bm25_weight_mult, sk))

        if verbose:
            print(f"      [{sk}] → {len(chunk_meta_store)} unique candidates so far")

    # ── 4. Metadata-boosted RRF Fusion ─────────────────────────────────────
    if verbose:
        print(f"[4/5] RRF fusion over {len(rrf_inputs)} ranked lists …")

    if not rrf_inputs:
        logger.warning("No retrieval results — returning empty result.")
        return RetrievalResult(
            query=query, mode=mode, plan=plan, chunks=[],
            total_candidates=0,
            latency_ms=(time.perf_counter() - t_start) * 1000,
        )

    fused = reciprocal_rank_fusion(
        ranked_lists       = [rl for rl, _, _ in rrf_inputs],
        weights            = [w  for _, w, _ in rrf_inputs],
        source_keys        = [sk for _, _, sk in rrf_inputs],
        high_value_signals = plan.high_value_signals,
    )
    total_candidates = len(fused)
    sorted_fused = sorted(fused.items(), key=lambda x: x[1][0], reverse=True)

    # ── 5. Score-based selection ────────────────────────────────────────────
    pool_size = min(top_k, total_candidates)
    final_pool = sorted_fused[:pool_size]

    if verbose:
        print(f"[5/5] Score-based selection ({pool_size} candidates → {top_k} final) …")

    # ── Assemble final chunks ──────────────────────────────────────────────
    final_chunks: list[RetrievedChunk] = []

    for cid, (rrf_score, meta) in final_pool:
        ns = chunk_namespace.get(cid, "")
        sk = chunk_sourcekey.get(cid, _infer_source_key(meta))

        chunk = _build_retrieved_chunk(
            chunk_id          = cid,
            meta              = meta,
            namespace         = ns,
            source_key        = sk,
            rrf_score         = rrf_score,
            retrieval_methods = sorted(chunk_methods.get(cid, {"dense"})),
        )
        chunk.score = round(rrf_score, 4)
        final_chunks.append(chunk)

    final_chunks.sort(key=lambda c: c.score, reverse=True)
    final_chunks = final_chunks[:top_k]

    source_breakdown: dict[str, int] = {}
    for c in final_chunks:
        source_breakdown[c.source_key] = source_breakdown.get(c.source_key, 0) + 1

    latency_ms = (time.perf_counter() - t_start) * 1000

    if verbose:
        print(f"\n✅ Done in {latency_ms:.0f} ms — {total_candidates} candidates → {len(final_chunks)} chunks")
        print(f"   Source breakdown: {source_breakdown}")
        for i, c in enumerate(final_chunks, 1):
            print(f"   [{i}] {c.score:.3f}  [{c.source_key}]  {c.citation_label[:55]}")

    return RetrievalResult(
        query            = query,
        mode             = mode,
        plan             = plan,
        chunks           = final_chunks,
        total_candidates = total_candidates,
        latency_ms       = latency_ms,
        source_breakdown = source_breakdown,
    )


# ─────────────────────────────────────────────────────────────────────────────
# Async wrapper
# ─────────────────────────────────────────────────────────────────────────────

async def aretrieve(
    query:      str,
    course_id:  str,
    mode:       RetrievalMode = RetrievalMode.ASK,
    top_k:      int  = 8,
    index_name: str | None = None,
    verbose:    bool = False,
) -> RetrievalResult:
    """Async wrapper — runs sync pipeline in a thread pool."""
    loop = asyncio.get_event_loop()
    return await loop.run_in_executor(
        None,
        lambda: retrieve(
            query=query,
            course_id=course_id,
            mode=mode,
            top_k=top_k,
            index_name=index_name,
            verbose=verbose,
        ),
    )


